# Math Functions
x = 3.14
y = -4
z = 5
result = round(x) # Round off the value of x.
print(result)
result = abs(y) # Absolute Value - Distance from 0 to number.
print(result)
result = pow(z,2) # Power the value to the base (base,power)
print(result)
result = max(x,y,z) # Maximum Value from input variables.
print(result)
result = min(x,y,z) # Minimum Value from input variables.
print(result)